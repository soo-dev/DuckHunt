package vo;

public class PointVo {

	int idx;
	String name;
	int gamepoint, helperpoint, rank;

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getGamepoint() {
		return gamepoint;
	}

	public void setGamepoint(int gamepoint) {
		this.gamepoint = gamepoint;
	}

	public int getHelperpoint() {
		return helperpoint;
	}

	public void setHelperpoint(int helperpoint) {
		this.helperpoint = helperpoint;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

}
